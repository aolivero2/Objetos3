    private void cmdCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdCalcularActionPerformed
        int opc, nz1,nz2,ni1,ni2;
        NumComplejos op1,op2,res=null;
        
        if (txtNumZ1.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Digite el numero entero 1", "Error", JOptionPane.ERROR_MESSAGE);
            txtNumZ1.requestFocusInWindow();
            

        } else if (txtNumi1.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Digite el numero imaginario 1", "Error", JOptionPane.ERROR_MESSAGE);
            txtNumi1.requestFocusInWindow();
            
        } else if (txtNumZ2.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Digite el numero entero 2", "Error", JOptionPane.ERROR_MESSAGE);
            txtNumZ2.requestFocusInWindow();
            

        } else if (txtNumi2.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Digite el numero imaginario 2", "Error", JOptionPane.ERROR_MESSAGE);
            txtNumi2.requestFocusInWindow();
           
         }else{ 
        opc=cmbOperaciones.getSelectedIndex();
        nz1=Integer.parseInt(txtNumZ1.getText());
        nz2=Integer.parseInt(txtNumZ2.getText());
        ni1=Integer.parseInt (txtNumi1.getText());
        ni2=Integer.parseInt (txtNumZ2.getText());
        
        
         op1= new NumComplejos (nz1,ni1);
         op2= new NumComplejos (nz2,ni2);
         
          switch (opc){
              case 0:
              res = op1.suma (op2);
                break;
              case 1:
                  res=op1.resta(op2);
                  break;
              case 2:
                 res=op1.multiplicacion(op2);
          }
          txtNumZ3.setText(""+res.getNumreal());
          txtNumi3.setText(""+res.getNuming());
          
          cmdCalcular.setEnabled(false);
          cmdBorrar.setEnabled(true);
        }
          
    
    }//GEN-LAST:event_cmdCalcularActionPerformed

